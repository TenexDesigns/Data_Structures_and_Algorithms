What is a data strucure

A data strucure is a way to store and organise data so that it can be used efficently
A data strucure is a collection of data values , the relationships among them and the functions or operations that can be applied to that data.





Why Learn Data strucure
The differnece between a function taking a few milliseconds  vs a few seconds or even minutes comes dwn to the selection of the right data structure.


Data Strucure help you solve problems in a more effcient way, both in terms of time and memory.


