    What are Algorithms?
An algorithm is a set of step-by-step procedures, or a set of rules to follow, for completing a specific task or solving a particular problem. Algorithms are all around us. The recipe for baking a cake, the method we use to solve a long division problem, and the process of doing laundry are all examples of an algorithm. In the context of computer science and programming, an algorithm is a process or set of rules to be followed in calculations or other problem-solving operations, especially by a computer poanchen.github.io.

